export function getStatistikDemografi(rt?: string) {
  const filtered = rt ? MOCK_WARGA.filter((w) => w.rt === rt) : MOCK_WARGA
  return {
    total: filtered.length,
    tetap: filtered.filter((w) => w.statusKependudukan === "Tetap").length,
    domisili: filtered.filter((w) => w.statusKependudukan === "Domisili").length,
    kontrak: filtered.filter((w) => w.statusKependudukan === "Kontrak").length,
    lakiLaki: filtered.filter((w) => w.jenisKelamin === "Laki-laki").length,
    perempuan: filtered.filter((w) => w.jenisKelamin === "Perempuan").length,
  }
}

export function getStatistikKeuangan(rt?: string) {
  const filtered = rt ? MOCK_TRANSAKSI.filter((t) => t.rt === rt) : MOCK_TRANSAKSI
  const masuk = filtered.filter((t) => t.jenis === "masuk").reduce((s, t) => s + t.jumlah, 0)
  const keluar = filtered.filter((t) => t.jenis === "keluar").reduce((s, t) => s + t.jumlah, 0)
  return {
    masuk,
    keluar,
    saldo: masuk - keluar,
    transaksi: filtered,
  }
}

