export function getKartuKeluargaList(): KartuKeluarga[] {
  const kkMap = new Map<string, KartuKeluarga>()
  MOCK_WARGA.forEach((w) => {
    if (!kkMap.has(w.noKK)) {
      kkMap.set(w.noKK, {
        noKK: w.noKK,
        kepalaKeluarga: "",
        alamat: w.alamat,
        rt: w.rt,
        rw: w.rw,
        jumlahAnggota: 0,
        tanggalDibuat: "2020-01-15",
        anggota: [],
      })
    }
    const kk = kkMap.get(w.noKK)!
    kk.anggota.push(w)
    kk.jumlahAnggota = kk.anggota.length
    if (w.hubunganKeluarga === "Kepala Keluarga") {
      kk.kepalaKeluarga = w.nama
    }
  })
  return Array.from(kkMap.values())
}

